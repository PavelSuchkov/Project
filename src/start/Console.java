package start;

public class Console {

    public static void main(String args[]) {

        int m =0 ;
        if (m > 0){
            m++;
            System.out.println(m);
        }
        if (m < 0){
            m = m - 2;
            System.out.println(m);
        }
        if (m == 0){
            m = 10;
            System.out.println(m);
        }
    }
}
